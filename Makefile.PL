use strict;
use ExtUtils::MakeMaker;

WriteMakefile ( NAME      => 'Proc::Application',
                VERSION   => '0.01',
                PREREQ_PM => { 'IO::Socket::SSL'  => '0.73' } );

package MY;

sub libscan
{
    my ( $this, $path ) = @_;
    return '' if $path =~ m/.(pl|dtd|sgml)$/;
    return '' if $path =~ m:\bCVS/:;
    return '' if $path =~ m/~$/;
    $path;
}

1;
